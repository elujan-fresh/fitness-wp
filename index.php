						<p class="orange" id="blog-unicorn">
							Unicorn in the Spotlight: Teri Ann Krefting
						</p>
						<p class="info" id="blog-info1">
							Posted by <span class="h1-blue">Tanja Djelevic </span> in <span class="h1-blue">Spotlight</span> on October 17th, 2013
						</p>
						<p id="blog-info2" class="info">
							I have always been a FAN of sharing the wealth, paying it forward and putting people I love and who inspire ME in the Spotlight. Fortunately I’ve been blessed with working and meeting some of the most amazing people on this earth. Some are BIG personalities in our industry and some are people who inspire their nearest surroundings because of their own life journey. Take a time to read my Spotlight series that starts with a simple few questions and answers that may inspire YOU to Live Life Loud and Pay It Forward!
						</p>
						<p id="blog-image">
							<img src="img/blog/blog_01.jpg">
						</p>
						<p id="blog-info3" class="info">
							I met <span class="h1-blue"><a href="#" >Teri Ann </a> </span> many years ago when we both worked at Crunch Fitness in Hollywood. The first thing that struck me with this beautiful blonde was her Outlook on the world around her. Always with a smile, and a CURIOSITY that is sometimes only found in kids… T.A is a bad ass trainer and teacher, she has trained anyone from Tom Cruise, to Lady GAGA (I’m probably not allowed to say that but hey…) and she touches everyone the same way-with her Sunshine excitement of giving and learning. T.A inspires me to be CURIOUS about life, and maybe she will inspire you to. T.A, you rock mama, and I’m excited to get on this journey with you!
						</p>
						
						<p class="orange" id="blog-info4">Who are you?</p>
						<p class="info">My Name is Teri Ann Krefting.</p>
						
						<p class="orange blog-margin-orange">What drives you?</p>
						<p class="info">Order. I’m totally OCD, so I like schedule and everything in its place.</p>
						
						<p class="orange blog-margin-orange">Who is your Role mode and why is that?</p>
						<p class="info">Honestly, I have lots of role models.  In my career, my role model is Tanja Djelevic (No lie, I know it sounds weird because this is her blog, but she really is).  She is just so passionate about fitness and helping people.  She inspires me to do better and to learn more.  I love her!     </p>
						
						<p class="orange blog-margin-orange">What is your best advice to heal sadness?</p>
						<p class="info">Do you have a few things you do, or people you see?  Exercise really helps.  I also go to the beach to watch the surfers and feel the sun on my skin.  When I’m sad, I’ll always call home and talk to my family.  They always know how to make me feel better.   </p>
						
						<p class="orange blog-margin-orange">What makes you happy to the Bone with Endless Smiles?</p>
						<p class="info">My nieces and nephews (when they aren’t being snotty).  It’s so fun and interesting to watch them discover new things and to hear them talk.  They have no filter and it’s kind of refreshing.</p>
						
						<p class="orange blog-margin-orange">Favorite place in the world?</p>
						<p class="info">I have tons of “favorite places” in the world, and I have tons more to discover.  One of my favorite places is this little surf town called New Smyrna Beach, FL.  My boyfriend grew up there, so I get to visit quite often.  It’s a slow moving town with great weather, a beach to surf, and a river to stand up paddle.</p>

						<p class="orange blog-margin-orange">What would you attempt do if you knew you could not fail?</p>
						<p class="info">Move to a vineyard by the beach, Make my own wine and olive oil, and surf everyday so I could get over my fear of big wave.</p>

						<p class="orange blog-margin-orange">I’m curious myself, and I’m sure others are too: Is there a moment, a time, a person, a situation in your work where you were going through it thinking: ”It freaking doesn’t get any better that this…”? Tell us, I want to inspire people with what you feel and think!</p>
						<p class="info">I lived in Turkey for 3 months teaching fitness classes and I was asked to be part of a wedding party.  I went to all of the festivities of the wedding and learned so much about Turkish tradition, food, and just being in the moment.</p>
						<br /><p class="info">That’s definitely a moment in my life where I thought, “Wow, my life couldn’t get any better than this”.</p>
